// implementation of student class
class student
{
    int roll;
    int age;

public:
    int getRoll()
    {
        return this->roll;
    }
    int getAge()
    {
        return this->age;
    }

    void setAge(int age)
    {
        this->age = age;
    }
    void setRoll(int roll)
    {
        this->roll = roll;
    }
};
